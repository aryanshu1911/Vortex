# Spinner/progress
def spinner(task, duration=2):
    spinner_chars = "|/-\\"
    print(f"{BLUE}[+] {task}...{RESET}", end=" ", flush=True)
    for i in range(duration * 4):
        print(spinner_chars[i % 4], end="\b", flush=True)
        time.sleep(0.25)
    print("‚úĒ")